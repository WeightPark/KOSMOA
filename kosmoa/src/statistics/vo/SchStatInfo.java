package statistics.vo;

public class SchStatInfo {
	private String st_id, st_name;
	private int st_count, sk_count, count_of_all;
	public String getSt_id() {
		return st_id;
	}
	public void setSt_id(String st_id) {
		this.st_id = st_id;
	}
	
	public String getSt_name() {
		return st_name;
	}
	public void setSt_name(String st_name) {
		this.st_name = st_name;
	}
	
	public int getSt_count() {
		return st_count;
	}
	public void setSt_count(int st_count) {
		this.st_count = st_count;
	}
	public int getSk_count() {
		return sk_count;
	}
	public void setSk_count(int sk_count) {
		this.sk_count = sk_count;
	}
	public int getCount_of_all() {
		return count_of_all;
	}
	public void setCount_of_all(int count_of_all) {
		this.count_of_all = count_of_all;
	}
	
	
	
	

}
